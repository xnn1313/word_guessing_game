DIFFICULTY_LABELS = {
    1: "入门",
    2: "熟练",
    3: "进阶",
    4: "挑战",
    5: "大师",
}


def _build_categories():
    categories = []
    for spec in CATEGORY_SPECS:
        if len(spec["targets"]) != 20:
            raise ValueError(f"分类 {spec['id']} 必须包含 20 关")
        levels = []
        for index, target in enumerate(spec["targets"]):
            difficulty = index // 4 + 1
            levels.append(
                {
                    "id": f"{spec['id']}-{index + 1}",
                    "name": f"{DIFFICULTY_LABELS[difficulty]}挑战 {index % 4 + 1}",
                    "target": target,
                    "difficulty": difficulty,
                    "difficulty_label": DIFFICULTY_LABELS[difficulty],
                }
            )
        categories.append({key: value for key, value in spec.items() if key != "targets"} | {"levels": levels})
    return categories


CATEGORIES = _build_categories()


def get_all_targets():
    return [level["target"] for category in CATEGORIES for level in category["levels"]]


def get_level(level_id):
    for category in CATEGORIES:
        for index, level in enumerate(category["levels"]):
            if level["id"] == level_id:
                return {
                    **level,
                    "category_id": category["id"],
                    "category_name": category["name"],
                    "category_emoji": category["emoji"],
                    "order": index + 1,
                }
    return None


def get_next_level(level_id):
    level = get_level(level_id)
    if not level:
        return None
    category = next(item for item in CATEGORIES if item["id"] == level["category_id"])
    next_index = level["order"]
    if next_index >= len(category["levels"]):
        return None
    return get_level(category["levels"][next_index]["id"])


def is_unlocked(level_id, progress):
    level = get_level(level_id)
    if not level:
        return False
    if level["order"] == 1:
        return True
    category = next(item for item in CATEGORIES if item["id"] == level["category_id"])
    previous_id = category["levels"][level["order"] - 2]["id"]
    return progress.get(previous_id, {}).get("stars", 0) > 0


def public_level(level, progress):
    result = progress.get(level["id"], {})
    return {
        "id": level["id"],
        "name": level["name"],
        "order": level["order"],
        "category_id": level["category_id"],
        "category_name": level["category_name"],
        "category_emoji": level["category_emoji"],
        "difficulty": level["difficulty"],
        "difficulty_label": level["difficulty_label"],
        "unlocked": is_unlocked(level["id"], progress),
        "stars": result.get("stars", 0),
        "best_attempts": result.get("best_attempts"),
    }


def public_catalog(progress):
    categories = []
    for category in CATEGORIES:
        levels = [public_level(get_level(level["id"]), progress) for level in category["levels"]]
        categories.append(
            {
                "id": category["id"],
                "name": category["name"],
                "emoji": category["emoji"],
                "description": category["description"],
                "stars": sum(level["stars"] for level in levels),
                "max_stars": len(levels) * 3,
                "levels": levels,
            }
        )
    return {
        "total_stars": sum(category["stars"] for category in categories),
        "max_stars": sum(category["max_stars"] for category in categories),
        "categories": categories,
    }


def calculate_stars(attempts):
    if attempts <= 5:
        return 3
    if attempts <= 10:
        return 2
    return 1
